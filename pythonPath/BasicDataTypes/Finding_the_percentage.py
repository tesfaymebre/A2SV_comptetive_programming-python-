if __name__ == '__main__':
    n = int(input())
    student_marks = {}
    for _ in range(n):
        name, *line = input().split()
        scores = list(map(float, line))
        student_marks[name] = scores
    query_name = input()

    marks = student_marks[query_name]
    sum = 0
    for pt in marks:
       sum = sum + pt
    print("{:.2f}".format(sum/len(marks)))
